x = 6
y = 2
z = 7

print(f"{x} + {y} is equal to: {x+y}")
print(f"{x} - {y} is equal to: {x-y}")
print(f"{x} * {y} is equal to: {x*y}")
print(f"{x} / {y} is equal to: {x/y}")

print(f"{z} / {y} is equal to {z//y}")
print(f"{z} % {y} is equal to {z%y}")
print(f"{z} raised to {y} is equal to {z**y}")
print(f"{z} raised to {3} is equal to {z**3}")
print(f"square root {x} is equal to {x**0.5}")


